class thuocTinh{
  String _hoTen;
  String _queQuan;
  int _namSinh;
  String _maKhoa;

  String get hoTen => _hoTen;

  set hoTen(String value) {
    _hoTen = value;
  }

  String get queQuan => _queQuan;

  String get maKhoa => _maKhoa;

  set maKhoa(String value) {
    _maKhoa = value;
  }

  int get namSinh => _namSinh;

  set namSinh(int value) {
    _namSinh = value;
  }

  set queQuan(String value) {
    _queQuan = value;
  }
}